public class DoWhile {
    public static void main(String[] args){
        int i = 100;
        do{
            System.out.println("Element: " + i);
        } while (i < 10);
        int i = 0;
        do{
            System.out.println("Element: " + i);
            i++;
        } while (i < 10);
    }
}
